package Arrays;
import java.util.*;
public class ArraySorting {
	public static void main(String[] args) {
		int a[]= {4,8,6,1,45,21,7};
		int temp=0;
		for(int i=0;i<a.length;i++)
		{
			for(int j=i+1;j<a.length;j++)
			{
				if(a[i]>a[j])
				{
					temp=a[i];
					a[i]=a[j];
					a[j]=temp;
				}
			}
		}
		System.out.println(Arrays.toString(a));
	}

}
